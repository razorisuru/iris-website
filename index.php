                <p style=" font-weight:600;background-color:rgb(60,16,83); color:white">Rector’s Message</p>
                <h6 style="font-weight:500;font-size:15px;line-height:24px"> I congratulate the staff of the department
                    of
                    English & Modern Languages of
                    SIBA Campus on the issue of IRIS volume II. IRIS is the SIBA forum for creative writers. It offers a
                    top-notch platform for creative writers to display their writing skills and hone their creative
                    competencies. The department of English & Modern Languages of SIBA does not promote conventional
                    language
                    teaching. Our mission is to promote English language learning for communicative purposes. As we
                    know,
                    today,
                    Commercial, industrial and service sectors are in the dire need of creative people and good
                    communicators.
                    As such, IRIS is yet another step towards producing graduates with creative thoughts and excellent
                    communication skills. Though SIBA is relatively a small university college, we have a clear vision
                    of
                    rendering quality education for the 21st century. </h6>
                <h6 style="font-weight:500;font-size:15px;line-height:24px"> I am happy to see that IRIS has produced a
                    bunch of
                    enthusiastic writers who
                    show high potentials of becoming full-fledged poets, short story writers and essayists in the
                    future. I
                    take
                    this opportunity to thank the editor-in-chief, the award winning, veteran writer Ms. Kamala
                    Wijerathne for
                    her efforts to fine tune the creative capacities of our writers by conducting creative writing
                    workshops.
                    I
                    also thank the members of the academic staff of the department of English & Modern Languages headed
                    by Ms.
                    Buddhi Ariyratne for bringing forth the second volume of IRIS Volume 2, despite the hindrances
                    caused by
                    the
                    epidemic situation in the country. All the writers who contributed to the second volume of IRIS
                    deserve
                    acknowledgement, too. The encouraging hand given by many others who supported in many ways to make
                    IRIS
                    volume 2 a reality is much appreciated.</h6>
