            <center style="font-family: 'Redressed'">
                <h3 style="font-weight:500">Welcome to the Creative Writing Journal IRIS - The SIBA Forum for Creative
                    Writers.
                </h3>
                <br>
                <h6 style="text-align:justify;font-size:20px;line-height:27px">IRIS is the annual creative writing
                    journal
                    published by the Department of
                    English & Modern Languages of the Sri Lanka International Buddhist Academy. The journal receives
                    creative
                    articles from undergraduates and academics from higher educational institutes in Sri Lanka and
                    abroad.
                </h6>
                <h6 style="text-align:justify;font-size:20px;line-height:27px">The name of the journal reflects the
                    aspirations
                    and the vision of the
                    architects of IRIS. The name Iris has multiple meanings. At the most familiar level it is the orb of
                    the
                    eye, the part that envisions and responds to all visual stimuli. At the classical level, it is the
                    goddess
                    of the rainbow, the varied hued and layered spectacle in the sky. The weaving together of the
                    physical,
                    the
                    natural, the aesthetic and the mythical has resulted in a mosaic of wonderful creations. The
                    visionary
                    behind this venture is Dr. Upali M. Sedera the Rector/CEO of SIBA.</h6>
                <h6 style="text-align:justify;font-size:20px;line-height:27px">Publication of IRIS by the department of
                    English
                    & modern Languages is aimed
                    at
                    providing a forum for writers to hone their skills in written communication. Presumably it was the
                    platform
                    for young writers, but at the same time it has attracted the attention of more mature academics and
                    writers
                    who have greatly contributed to the success of IRIS. </h6>
                <h6 style="text-align:justify;font-size:20px;line-height:27px">The editorial team cordially invite you
                    to be a
                    proud member of IRIS writers’
                    Club by sharing your creative thoughts and insights in the form of a story, an essay, poetry, a
                    review or
                    even graphics.</h6>
            </center>
            <br>
