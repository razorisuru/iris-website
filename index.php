                <p style=" font-weight:600;background-color:rgb(60,16,83); color:white; font-family:Cambria;">Editorial
                </p>
                <h6 style="text-align:justify;font-size:20px;line-height:27px"> It gives me immense pleasure to write
                    this
                    editorial for Iris Volume II. In
                    one year we have grown visibly. The contribution, the students have made towards this volume is
                    ample
                    proof
                    of the successfulness of the journal. The poetry has gained in both breadth and depth. More poets
                    have
                    been
                    created. Prose has gained in diversity. All this in spite of the overarching shadow of the Covid 19.
                    It
                    has
                    however restricted our reach. We have managed to get poems from an internationally reputed poet from
                    Nepal.
                    Dr. Keshab Sigdel has been generous to share his poems with us. I have great pleasure in including
                    three
                    of
                    his poems in this volume. </h6>
                <h6 style="text-align:justify;font-size:20px;line-height:27px"> The editorial board headed by Ruvini
                    Wickramaratne has to be commended once
                    again with her special talent to sustain the enthusiasm of her colleagues and students, she has
                    inspired
                    her
                    students to produce work of quality. Iris, the all-seeing orb has opened the doors for observing,
                    dissecting
                    and analyzing human experiences. Let us keep going with what has been achieved and strive to be
                    better in
                    the future. </h6>
                <br>
